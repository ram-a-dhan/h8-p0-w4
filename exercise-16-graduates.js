function graduates (students) {
    
    var graduation = {}, alreadyInList;
    for (var i = 0; i < students.length; i++) {
        alreadyInList = false;
        for (var key1 in graduation) {
            if (students[i].class === key1) {alreadyInList = true;}
        }
        if (!alreadyInList) {graduation[students[i].class] = [];}
    }
    // return graduation;

    for (var key2 in graduation) {
        for (j = 0; j < students.length; j++) {
            if (key2 === students[j].class && students[j].score > 75) {
                graduation[key2].push({
                    name: students[j].name,
                    score: students[j].score,
                });
            }
        }
    }
    return graduation;

}
  
